           ข้อ 5 ผู้รับทุนจะต้องจัดทำรายงานความก้าวหน้าของการดำเนินงานวิจัยเสนอต่อมหาวิทยาลัย ทุก 3  เดือน หรือตามที่ผู้ให้ทุนเห็นสมควร นับตั้งแต่วันที่ทำสัญญานี้เป็นต้นไป  และส่งรายงานการวิจัยฉบับสมบูรณ์ให้แก่มหาวิทยาลัยเมื่อครบกำหนดสัญญา  ตามรายละเอียดที่มหาวิทยาลัยกำหนด
    ข้อ 6  ผู้รับทุนต้องเผยแพร่ผลงานวิจัยภายในระยะเวลา 1 ปี หลังสิ้นสุดสัญญา โดยการตีพิมพ์ในวารสารวิชาการ ตามประกาศ ก.พ.อ. เรื่องหลักเกณฑ์การพิจารณาวารสารทางวิชาการ สำหรับการเผยแพร่ผลงานทางวิชาการ พ.ศ. 2562 หรือนำผลงานวิจัยไปสู่การใช้ประโยชน์อย่างเป็นรูปธรรมหรือเป็นที่ประจักษ์ตามข้อเสนอโครงการกับกลุ่มหรือชุมชนเป้าหมายโดยมีหลักฐานรับรองการใช้ประโยชน์ 
        ข้อ 7 หากผู้รับทุนไม่สามารถส่งรายงานผลการวิจัยฉบับสมบูรณ์ได้ภายในระยะเวลาที่ระบุใน ข้อ 5  ให้ผู้รับทุนแจ้งเหตุผลให้ผู้ให้ทุนทราบเป็นลายลักษณ์อักษรและขออนุมัติขยายเวลาดำเนินการวิจัยก่อนวันสิ้นสุดสัญญาไม่น้อยกว่า 15 วัน โดยผู้ให้ทุนทรงไว้ซึ่งสิทธิที่จะพิจารณาอนุมัติให้ขยายเวลาหรือไม่ก็ได้  เมื่อขยายเวลาแล้วยังไม่สามารถส่งผลงานวิจัยได้ภายในระยะเวลาที่กำหนดหรือผิดสัญญา  มหาวิทยาลัยมีสิทธิที่จะเรียกเงินทุนที่ผู้วิจัยได้รับคืนเต็มจำนวนพร้อมดอกเบี้ยร้อยละสิบต่อปีนับแต่วันเริ่มต้นดำเนินการวิจัยตามสัญญา และระงับการให้ทุนอุดหนุนงานวิจัยหรืองานสร้างสรรค์จากมหาวิทยาลัยราชภัฏยะลา
        ข้อ 8 ผู้รับทุนจะต้องไม่เปลี่ยนเรื่องที่ทำการวิจัย  โดยไม่ได้รับอนุมัติจากผู้ให้ทุน มิฉะนั้นผู้รับทุนจะต้องคืนเงินทุนพร้อมดอกเบี้ยไม่เกินร้อยละสิบต่อปีนับแต่วันเริ่มต้นดำเนินการวิจัยตามสัญญา
        ข้อ 9 กรณีผู้รับทุนประสงค์ที่จะคืนเงินทุนที่ได้หลังวันทำสัญญาโดยไม่มีเหตุผลอันสมควร   มหาวิทยาลัยมีสิทธิเรียกทุนคืนพร้อมทั้งดอกเบี้ยไม่เกินร้อยละสิบต่อปีนับตั้งแต่เริ่มต้นดำเนินการวิจัยตามสัญญา
        ข้อ 10 หากมีข้อขัดข้องใดๆ นอกเหนือจากที่ระบุไว้ตามสัญญาดังกล่าวข้างต้น ให้อยู่ในดุลยพินิจของผู้ให้ทุนที่จะพิจารณาดำเนินการต่อไป
        ข้อ 11 ให้จัดทำสัญญานี้ขึ้นเป็นจำนวน 2 ฉบับ เก็บไว้ที่ผู้รับทุนและสถาบันวิจัยและพัฒนาชายแดนภาคใต้ แห่งละ 1 ฉบับ
        
    
    
    
    
    
