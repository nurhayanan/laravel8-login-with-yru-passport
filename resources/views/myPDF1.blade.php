        สัญญานี้ทำขึ้น ณ มหาวิทยาลัยราชภัฏยะลา  ตั้งอยู่เลขที่ 133  ถนนเทศบาล 3  ตำบลสะเตง  อำเภอเมืองยะลา  จังหวัดยะลา  เมื่อวันที่..........เดือน......................พ.ศ. ................. ระหว่างมหาวิทยาลัยราชภัฏยะลา  โดย ผู้ช่วยศาสตราจารย์ ดร. สมบัติ  โยธาทิพย์ อธิการบดีมหาวิทยาลัยราชภัฏยะลา  ซึ่งต่อไปในสัญญานี้เรียกว่า  “ผู้ให้ทุน”   ฝ่ายหนึ่งกับ  นาย/นาง/น.ส. ................... .....................................................................หมายเลขบัตรประชาชน _ - _ _ _ _ - _ _ _ _ _ - _ _ -_ตำแหน่ง...................................................................สังกัดหลักสูตร.....................................................คณะ/สถาบัน/สำนัก...................................................... มหาวิทยาลัยราชภัฏยะลาซึ่งต่อไปในสัญญานี้เรียกว่า “ผู้รับทุน” อีกฝ่ายหนึ่ง  คู่สัญญาได้ตกลงกัน  ดังต่อไปนี้
