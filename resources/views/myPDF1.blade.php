        ข้อ 2 ผู้ให้ทุนตกลงจะจ่ายเงินให้แก่ผู้รับทุนจำนวน 2 งวด และในการจ่ายเงินอุดหนุนให้แก่ผู้รับทุนแต่ละงวดตามกำหนดเวลาและเงื่อนไขของประกาศหลักเกณฑ์ทุนอุดหนุนงานวิจัยหรืองานสร้างสรรค์ของมหาวิทยาลัย ทั้งนี้ให้ผู้รับทุนเบิกจ่ายเงินและควบคุมการใช้จ่ายเงินให้เป็นไปตามระเบียบกระทรวงการคลังและประกาศมหาวิทยาลัย 
            2.1 งวดที่หนึ่ง ผู้รับทุนจะได้รับเงินร้อยละห้าสิบของงบประมาณทั้งโครงการ เป็นเงินจำนวน.........................บาท  (...........................................................................................................................) ซึ่งจะจ่ายให้แก่ผู้รับทุนเมื่อผู้รับทุนลงนามในสัญญา 
        2.2 งวดที่สอง ผู้รับทุนจะได้รับเงินร้อยละห้าสิบของงบประมาณทั้งโครงการ เป็นเงินจำนวน.........................บาท  (...........................................................................................................................) ซึ่งจะจ่ายให้แก่ผู้รับทุนเมื่อผู้รับทุนทำงานวิจัยเสร็จสมบูรณ์  และผู้รับทุนได้ส่งรายงานวิจัยฉบับสมบูรณ์ 2 เล่ม พร้อมซีดีบันทึกข้อมูล 1 แผ่น รายงานผลการปรับปรุงงานวิจัย (แบบ สวพ.รง.3)
        ในกรณีงานวิจัยหรืองานสร้างสรรค์ที่มีข้อแนะนำให้ปรับแก้จากผู้ทรงคุณวุฒิ ให้นักวิจัยดำเนินการปรับแก้ หากไม่ปรับแก้ต้องแสดงเหตุผลประกอบตามรายงานปรับปรุงงานวิจัย (แบบ สวพ.รง.3)
    ข้อ 3 ผู้รับทุนจะดำเนินการวิจัยตามวิธีการที่เสนอไว้ในเอกสารโครงการวิจัยให้ครบถ้วนสมบูรณ์ และจะมอบให้บุคลากรตามเอกสารรายชื่อคณะผู้วิจัยเป็นผู้ทำงานในโครงการวิจัยนี้  หากเกิดอุปสรรคไม่สามารถดำเนินการวิจัยได้  หรือมีความจำเป็นจะต้องแก้ไขเปลี่ยนแปลงตัวบุคคล  แผนงาน  และวิธีการต่างๆ  ผู้รับทุน (หัวหน้าโครงการ) จะแจ้งให้ผู้ให้ทุนทราบก่อนภายใน 30 วัน แต่ทั้งนี้ต้องมีระยะเวลาคงเหลือในสัญญาไม่น้อยกว่า 60 วัน โดยผู้ให้ทุนสงวนสิทธิที่จะยุติการสนับสนุน หรือให้ความเห็นชอบกับการแก้ไขเปลี่ยนแปลงโครงการตามความเหมาะสม
        ข้อ 4 ในวันทำสัญญานี้ ผู้รับทุนต้องให้ข้าราชการพลเรือนในสถาบันอุดมศึกษา พนักงานมหาวิทยาลัย หรือพนักงานราชการ สังกัดมหาวิทยาลัยราชภัฏยะลา เป็นผู้ค้ำประกัน เพื่อเป็นการประกันความเสียหายกรณีผู้รับทุนได้ก่อให้เกิดขึ้นแก่ผู้ให้ทุน จำนวนหนึ่งคนต่อทุนอุดหนุนที่ได้รับจำนวนห้าแสนหรือไม่เกินห้าแสนบาท โดยผู้ค้ำประกันหนึ่งคน สามารถค้ำประกันผู้ขอรับทุนอุดหนุนงานวิจัยได้ไม่เกินสองโครงการ ดังรายชื่อต่อไปนี้
